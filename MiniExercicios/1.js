function adicao(a, b) {
  return a + b;
}

function subtracao(a, b) {
  return a - b;
}

function divisao(a, b) {
  return a / b;
}

function multiplicacao(a, b) {
  return a * b;
}

function exponenciacao(a, b) {
  return a ** b;
}

function modulacao(a, b) {
  return a % b;
}

console.log(adicao(2, 5));
console.log(subtracao(2, 5));
console.log(multiplicacao(2, 5));
console.log(divisao(2, 5));
console.log(exponenciacao(2, 5));
console.log(modulacao(2, 5));
